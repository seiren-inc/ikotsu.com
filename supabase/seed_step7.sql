受領時に固有の管理番号を付与し、全工程で番号紐付け管理を行います。粉骨機は1件ごとに完全清掃してから次の作業に入るため、他の方の遺骨との混入は構造的に防いでいます。

## Q2. 粉骨証明書はもらえますか？何が書かれていますか？

オプション（2,200円）で発行します。管理番号・依頼者氏名・作業日・工程グレード・担当者名・微粒子規格（2mm以下確認）・六価クロム除去の実施確認を記載します。

## Q3. お墓から取り出した遺骨の場合、追加の工程はありますか？

はい。墓から取り出した遺骨はカビや六価クロムが含まれる場合があるため、洗骨（六価クロム除去含む）→乾燥→粉骨の順で対応します。所要日数（5〜7日）と料金が増加します。

## Q4. 六価クロムとは何ですか？取り除いてもらえますか？

六価クロムは土中に含まれる有害物質で、墓から掘り出した遺骨に付着している場合があります。洗骨プランには六価クロム除去工程が含まれており、処理完了を証明書に記載します。

## Q5. 費用以外に追加費用は発生しますか？

お見積り時にご案内した料金以外の追加費用は原則発生しません。新たな工程追加が必要になった場合は、必ず事前に説明し、同意を得てから対応します。

## Q6. 遺骨の一部だけ粉骨することは可能ですか？

可能です。手元供養用に一部を残し、残りを粉骨するなど柔軟に対応できます。依頼時にご希望の分量をお伝えください。

## Q7. 粉骨後の遺骨はどのように返ってきますか？

専用の密封保存袋に収め、エアキャップと専用箱で丁寧に梱包して返送します。作業報告書または粉骨証明書を同梱します。

